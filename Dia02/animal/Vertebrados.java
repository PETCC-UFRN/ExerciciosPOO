package dia2.animal;

public class Vertebrados extends Animal {
	int quantidadeDeMúsculo;
	int quantidadeDeEsqueleto;
	
	public Vertebrados() {
		
	}
	public int getQuantidadeDeMúsculo() {
		return quantidadeDeMúsculo;
	}
	public void setQuantidadeDeMúsculo(int quantidadeDeMúsculo) {
		this.quantidadeDeMúsculo = quantidadeDeMúsculo;
	}
	public int getQuantidadeDeEsqueleto() {
		return quantidadeDeEsqueleto;
	}
	public void setQuantidadeDeEsqueleto(int quantidadeDeEsqueleto) {
		this.quantidadeDeEsqueleto = quantidadeDeEsqueleto;
	}
	
}
